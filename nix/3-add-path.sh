echo

echo '
### set PATH so it includes nix profile bin if it exists
if [ -d "$HOME/.nix-profile/bin" ] ; then
    PATH="$HOME/.nix-profile/bin:$PATH"
fi' | tee -a "${HOME}/.profile"

echo
